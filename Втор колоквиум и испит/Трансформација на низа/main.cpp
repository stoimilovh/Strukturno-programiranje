#include <stdio.h>

int praznaniza(int a[], int n){
    int i, temp=1;
    for(i=0; i<n; i++){
        if(a[i]!=0)
            return 0;
    }
    return 1;
}

int pozitiven(int a[], int n, int k, int h){
    int i;
    if(h==k)
        return 0;
    else
        for(i=n-1; i>=0; i--){
            a[i+1]=a[i];}
    a[0]=0;
    return pozitiven(a, n, k, h+1);
}

int negativen (int a[], int n, int k, int h){
    int i;
    if(h==k)
        return 0;
    else
        for(i=0; i<n; i++){
            a[i-1]=a[i];}
    a[n-1]=0;
    return negativen(a, n, k, h-1);
}

int main()
{
    int m, n, i, j, a[100], k, h=0, kraj=0;
    scanf("%d", &m);
    for(j=0; j<m; j++){
        scanf("%d", &n);
        for(i=0; i<n; i++){
            scanf("%d", &a[i]);
        }
        k=a[0];
        if(k>0){
            pozitiven(a, n, k, h);
        }
        else if(k<0){
            negativen(a, n, k, h);
        }
        for(i=0; i<n; i++){
            printf("%d ", a[i]);
        }
        printf("\n");
        h=0;
        kraj+=praznaniza(a, n);
    }
    printf("%d", kraj);
    return 0;
}

