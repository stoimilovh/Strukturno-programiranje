//за онолку места во лево, колку што е вредноста на елементот, празнините што ќе настанат во низата да се пополнат
//со елементи со вредност нула.
//
//При поместување на елементите, бројот на елементите во низата останува непроменет.
//На крајот да се испечатат трансформираните низи како и тоа колку од влезните низи останале празни,
//односно по трансформацијата имаат само елементи со вредност нула.
//
//Трансформацијата на низите да се реализира во посебна функција.
